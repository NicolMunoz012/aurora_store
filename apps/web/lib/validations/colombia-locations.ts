/**
 * Get municipalities for a specific department
 */
export function getMunicipalitiesForDepartment(departmentName: string): Municipality[] {
  const dept = COLOMBIAN_DEPARTMENTS.find((d) => d.name === departmentName);
  return dept?.municipalities ?? [];
}

/**
 * Get all department names
 */
export function getAllDepartmentNames(): string[] {
  return COLOMBIAN_DEPARTMENTS.map((d) => d.name);
}

/**
 * Validate if a municipality exists for a given department
 */
export function validateMunicipality(departmentName: string, municipalityName: string): boolean {
  const municipalities = getMunicipalitiesForDepartment(departmentName);
  return municipalities.some((m) => m.name === municipalityName);
}
